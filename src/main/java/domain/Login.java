package domain;

public class Login {
	private Integer id;
	private String userId;
	private String pass;

	public Integer getId() {
		return id;
	}

	public String getUserId() {
		return userId;
	}

	public String getPass() {
		return pass;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

}
